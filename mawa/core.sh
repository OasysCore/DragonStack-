# Playbook states: PILOT -> BETA -> STABLE -> SOTA
PLAYBOOK_STATE_PILOT="pilot"
PLAYBOOK_STATE_BETA="beta"
PLAYBOOK_STATE_STABLE="stable"
PLAYBOOK_STATE_SOTA="sota"  # State of the Art

# Initialize playbook for a skill
init_playbook() {
    local skill_name="$1"
    local initial_strategy="$2"
    
    local playbook_file="${MAWA_PLAYBOOK_DIR}/${skill_name}.playbook"
    
    cat > "$playbook_file" << EOF
# Playbook / 执行策略
# Skill: ${skill_name}
# Evolution: PILOT → BETA → STABLE → SOTA

metadata:
  skill: ${skill_name}
  created_at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")
  current_state: ${PLAYBOOK_STATE_PILOT}
  version: 1.0.0
  
strategy:
${initial_strategy}

execution:
  max_retries: 3
  timeout_seconds: 300
  require_confirmation: false
  
evolution:
  pilot:
    description: "Initial implementation, manual review required"
    auto_promote_after: 10
    success_rate_threshold: 0.7
    
  beta:
    description: "Tested implementation, reduced oversight"
    auto_promote_after: 50
    success_rate_threshold: 0.85
    
  stable:
    description: "Proven implementation, minimal oversight"
    auto_promote_after: 100
    success_rate_threshold: 0.95
    
  sota:
    description: "State of the Art, self-improving"
    continuous_learning: true
    
metrics:
  total_executions: 0
  successful_executions: 0
  failed_executions: 0
  average_duration_ms: 0
  last_execution_at: null
EOF

    echo -e "${MAWA_BLUE}📖 Initialized playbook for: ${skill_name} (${PLAYBOOK_STATE_PILOT})${MAWA_NC}"
}

# Get current playbook state
get_playbook_state() {
    local skill_name="$1"
    local playbook_file="${MAWA_PLAYBOOK_DIR}/${skill_name}.playbook"
    
    if [ -f "$playbook_file" ]; then
        grep "current_state:" "$playbook_file" | head -1 | awk '{print $2}'
    else
        echo "$PLAYBOOK_STATE_PILOT"
    fi
}

# Update playbook metrics
update_playbook_metrics() {
    local skill_name="$1"
    local success="$2"  # true/false
    local duration_ms="$3"
    
    local playbook_file="${MAWA_PLAYBOOK_DIR}/${skill_name}.playbook"
    
    if [ ! -f "$playbook_file" ]; then
        return 1
    fi
    
    # Update execution counts
    local total=$(grep "total_executions:" "$playbook_file" | awk '{print $2}')
    total=$((total + 1))
    
    local successful=$(grep "successful_executions:" "$playbook_file" | awk '{print $2}')
    local failed=$(grep "failed_executions:" "$playbook_file" | awk '{print $2}')
    
    if [ "$success" = "true" ]; then
        successful=$((successful + 1))
    else
        failed=$((failed + 1))
    fi
    
    # Update file using awk for cross-platform compatibility
    awk -v tot="$total" -v suc="$successful" -v fail="$failed" -v ts=$(date -u +"%Y-%m-%dT%H:%M:%SZ") '
        { 
            gsub(/total_executions: .*/, "total_executions: " tot)
            gsub(/successful_executions: .*/, "successful_executions: " suc)
            gsub(/failed_executions: .*/, "failed_executions: " fail)
            gsub(/last_execution_at: .*/, "last_execution_at: " ts)
            print
        }
    ' "$playbook_file" > "${playbook_file}.tmp"
    mv "${playbook_file}.tmp" "$playbook_file"
    
    # Check for evolution
    check_playbook_evolution "$skill_name"
}

# Check if playbook should evolve to next state
check_playbook_evolution() {
    local skill_name="$1"
    local playbook_file="${MAWA_PLAYBOOK_DIR}/${skill_name}.playbook"
    
    local current_state=$(get_playbook_state "$skill_name")
    local total=$(grep "total_executions:" "$playbook_file" | awk '{print $2}')
    local successful=$(grep "successful_executions:" "$playbook_file" | awk '{print $2}')
    
    if [ "$total" -eq 0 ]; then
        return 0
    fi
    
    local success_rate=$(echo "scale=2; $successful / $total" | bc)
    
    case "$current_state" in
        "$PLAYBOOK_STATE_PILOT")
            if [ "$total" -ge 10 ] && [ "$(echo "$success_rate >= 0.7" | bc -l)" -eq 1 ]; then
                awk -v state="$PLAYBOOK_STATE_BETA" '{ gsub(/current_state: .*/, "current_state: " state); print }' "$playbook_file" > "${playbook_file}.tmp"
                mv "${playbook_file}.tmp" "$playbook_file"
                echo "${MAWA_GREEN}🎉 Playbook evolved: ${skill_name} → ${PLAYBOOK_STATE_BETA}${MAWA_NC}"
            fi
            ;;
        "$PLAYBOOK_STATE_BETA")
            if [ "$total" -ge 50 ] && [ "$(echo "$success_rate >= 0.85" | bc -l)" -eq 1 ]; then
                awk -v state="$PLAYBOOK_STATE_STABLE" '{ gsub(/current_state: .*/, "current_state: " state); print }' "$playbook_file" > "${playbook_file}.tmp"
                mv "${playbook_file}.tmp" "$playbook_file"
                echo "${MAWA_GREEN}🎉 Playbook evolved: ${skill_name} → ${PLAYBOOK_STATE_STABLE}${MAWA_NC}"
            fi
            ;;
        "$PLAYBOOK_STATE_STABLE")
            if [ "$total" -ge 100 ] && [ "$(echo "$success_rate >= 0.95" | bc -l)" -eq 1 ]; then
                awk -v state="$PLAYBOOK_STATE_SOTA" '{ gsub(/current_state: .*/, "current_state: " state); print }' "$playbook_file" > "${playbook_file}.tmp"
                mv "${playbook_file}.tmp" "$playbook_file"
                echo "${MAWA_PURPLE}🏆 Playbook evolved: ${skill_name} → ${PLAYBOOK_STATE_SOTA} (State of the Art)${MAWA_NC}"
            fi
            ;;
    esac
}

