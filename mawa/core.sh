# Register a skill with its capabilities
register_skill() {
    local skill_name="$1"
    local skill_version="$2"
    local capabilities="$3"
    local constraints="$4"
    
    local reg_file="${MAWA_REGISTRY_DIR}/${skill_name}.reg"
    
    cat > "$reg_file" << EOF
# Skill Registration / 技能注册
# Generated by MAWA Registration System

skill:
  name: ${skill_name}
  version: ${skill_version}
  registered_at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")
  
capabilities:
${capabilities}

constraints:
${constraints}

boundaries:
  # Hard boundaries - what this skill CANNOT do
  cannot:
    - execute_destructive_commands_without_confirmation
    - access_sensitive_credentials_directly
    - bypass_user_approval_for_external_actions
    
  # Soft boundaries - requires additional confirmation
  requires_confirmation:
    - git_push_to_production
    - database_modifications
    - api_key_generation
EOF

    echo -e "${MAWA_GREEN}✅ Registered skill: ${skill_name} v${skill_version}${MAWA_NC}"
}

# Get skill capabilities
get_skill_capabilities() {
    local skill_name="$1"
    local reg_file="${MAWA_REGISTRY_DIR}/${skill_name}.reg"
    
    if [ -f "$reg_file" ]; then
        grep -A 20 "capabilities:" "$reg_file" | grep "  -" | sed 's/  - //'
    else
        echo ""
    fi
}

# Check if skill can perform action
can_skill_perform() {
    local skill_name="$1"
    local action="$2"
    
    local capabilities=$(get_skill_capabilities "$skill_name")
    
    if echo "$capabilities" | grep -q "$action"; then
        return 0
    else
        return 1
    fi
}

