# ATC: Agent Task Card (W/H/A/T)
# W - What/Why: 任务目标和原因
# H - How: 执行步骤
# A - Automation: 自动化程度
# T - Test/Verify: 验证方式

# Parse natural language to ATC
parse_natural_language() {
    local input="$1"
    
    # Simple keyword-based parsing (can be enhanced with NLP)
    local what=""
    local how=""
    local automation="partial"
    local test=""
    
    # Detect intent
    if echo "$input" | grep -qi "brainstorm\|idea\|构思"; then
        what="brainstorm_product_ideation"
        how="generate_ideas_and_concepts"
        suggested_skill="brainstorm"
    elif echo "$input" | grep -qi "design\|架构\|设计"; then
        what="architecture_design"
        how="create_technical_specification"
        suggested_skill="design"
    elif echo "$input" | grep -qi "code\|write\|implement\|实现"; then
        what="code_generation"
        how="generate_implementation_code"
        suggested_skill="code"
    elif echo "$input" | grep -qi "review\|检查\|审查"; then
        what="code_review"
        how="analyze_code_quality"
        suggested_skill="code-review"
    elif echo "$input" | grep -qi "test\|测试"; then
        what="test_planning"
        how="create_test_strategy"
        suggested_skill="test"
    elif echo "$input" | grep -qi "ship\|deploy\|发布\|部署"; then
        what="release_deployment"
        how="git_commit_and_push"
        suggested_skill="ship"
    elif echo "$input" | grep -qi "debug\|fix\|bug\|修复"; then
        what="debugging"
        how="diagnose_and_fix_issues"
        suggested_skill="debug"
    elif echo "$input" | grep -qi "doc\|document\|文档"; then
        what="documentation"
        how="generate_project_docs"
        suggested_skill="doc"
    elif echo "$input" | grep -qi "refactor\|重构\|优化"; then
        what="code_refactoring"
        how="improve_code_structure"
        suggested_skill="refactor"
    else
        what="general_task"
        how="analyze_and_execute"
        suggested_skill="brainstorm"
    fi
    
    echo "ATC:"
    echo "  W (What/Why): $what"
    echo "  H (How): $how"
    echo "  A (Automation): $automation"
    echo "  T (Test): validation_required"
    echo "  Suggested Skill: $suggested_skill"
}

# Dispatch task to appropriate skill
dispatch_task() {
    local user_input="$1"
    local taskrun_id="$2"
    
    echo "${MAWA_CYAN}🎯 Dispatcher: Analyzing task...${MAWA_NC}"
    
    # Simple keyword-based dispatch
    local suggested_skill="brainstorm"
    
    if echo "$user_input" | grep -qiE "(design|架构|设计|schema)"; then
        suggested_skill="design"
    elif echo "$user_input" | grep -qiE "(code|write|implement|实现|生成.*代码|写.*代码)"; then
        suggested_skill="code"
    elif echo "$user_input" | grep -qiE "(review|检查|审查)"; then
        suggested_skill="code-review"
    elif echo "$user_input" | grep -qiE "(test|测试)"; then
        suggested_skill="test"
    elif echo "$user_input" | grep -qiE "(ship|deploy|发布|部署)"; then
        suggested_skill="ship"
    elif echo "$user_input" | grep -qiE "(debug|fix|bug|修复)"; then
        suggested_skill="debug"
    elif echo "$user_input" | grep -qiE "(doc|document|文档)"; then
        suggested_skill="doc"
    elif echo "$user_input" | grep -qiE "(refactor|重构|优化)"; then
        suggested_skill="refactor"
    elif echo "$user_input" | grep -qiE "(brainstorm|idea|构思|想法)"; then
        suggested_skill="brainstorm"
    fi
    
    echo "${MAWA_BLUE}📋 ATC Parsed:${MAWA_NC}"
    echo "  W (What/Why): user_request"
    echo "  H (How): execute_${suggested_skill}_skill"
    echo "  A (Automation): partial"
    echo "  T (Test): validation_required"
    echo "  Suggested Skill: ${suggested_skill}"
    
    # Check if skill is registered
    local reg_file="${MAWA_REGISTRY_DIR}/${suggested_skill}.reg"
    if [ ! -f "$reg_file" ]; then
        echo "${MAWA_YELLOW}⚠️  Skill not registered: ${suggested_skill}${MAWA_NC}"
        echo "${MAWA_YELLOW}   Auto-registering...${MAWA_NC}"
        auto_register_skill "$suggested_skill"
    fi
    
    # Get playbook state
    local playbook_state=$(get_playbook_state "$suggested_skill")
    echo "${MAWA_BLUE}📖 Playbook State: ${playbook_state}${MAWA_NC}"
    
    # Log dispatch decision
    if [ -n "$taskrun_id" ]; then
        log_taskrun_step "$taskrun_id" "dispatch" "decision" "routed_to_${suggested_skill}"
    fi
    
    # Return suggested skill
    echo "$suggested_skill"
}

# Auto-register a skill with default capabilities
auto_register_skill() {
    local skill_name="$1"
    
    case "$skill_name" in
        "brainstorm")
            register_skill "$skill_name" "3.0.0" \
                "  - product_ideation
  - feature_planning
  - concept_generation
  - user_story_creation" \
                "  - requires_user_input: true
