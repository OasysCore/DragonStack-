            register_skill "$skill_name" "3.0.0" \
                "  - architecture_design
  - api_specification
  - database_schema
  - system_diagrams" \
                "  - requires_approval: true
  - max_complexity: enterprise"
            ;;
        "code")
            register_skill "$skill_name" "3.0.0" \
                "  - code_generation
  - multi_language_support
  - framework_integration
  - acp_agent_integration" \
                "  - languages: go,python,javascript,typescript,rust
  - requires_review: true"
            ;;
        "code-review")
            register_skill "$skill_name" "3.0.0" \
                "  - static_analysis
  - security_scanning
  - best_practices_check
  - performance_review" \
                "  - severity_threshold: medium
  - auto_fix: false"
            ;;
        "test")
            register_skill "$skill_name" "3.0.0" \
                "  - test_planning
  - unit_test_generation
  - integration_testing
  - coverage_analysis" \
                "  - min_coverage: 80%
  - test_frameworks: multiple"
            ;;
        "ship")
            register_skill "$skill_name" "3.0.0" \
                "  - git_operations
  - release_management
  - changelog_generation
  - deployment_automation" \
                "  - protected_branches: main,master
  - require_ci_pass: true"
            ;;
        "debug")
            register_skill "$skill_name" "3.0.0" \
                "  - error_analysis
  - log_parsing
