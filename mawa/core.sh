# Start a new task run
start_taskrun() {
    local task_name="$1"
    local skill_name="$2"
    local user_input="$3"
    
    local taskrun_id="tr_$(date +%s)_$(openssl rand -hex 4 2>/dev/null || echo $$)"
    local taskrun_file="${MAWA_TASKRUN_DIR}/${taskrun_id}.yaml"
    
    cat > "$taskrun_file" << EOF
# TaskRun / 任务执行记录
# ID: ${taskrun_id}

metadata:
  taskrun_id: ${taskrun_id}
  task_name: ${task_name}
  skill: ${skill_name}
  started_at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")
  status: running
  
input:
  user_command: "${user_input}"
  
execution_chain: []

output:
  result: null
  artifacts: []
  
metrics:
  duration_ms: 0
  tokens_used: 0
  api_calls: 0
EOF

    echo "$taskrun_id"
}

# Log execution step
log_taskrun_step() {
    local taskrun_id="$1"
    local step_name="$2"
    local step_type="$3"  # skill, agent, tool, decision
    local details="$4"
    
    local taskrun_file="${MAWA_TASKRUN_DIR}/${taskrun_id}.yaml"
    
    if [ ! -f "$taskrun_file" ]; then
        return 1
    fi
    
    # Create temp file with new step appended
    local temp_file="${taskrun_file}.tmp"
    awk -v step="$step_name" -v stype="$step_type" -v ts="$(date -u +"%Y-%m-%dT%H:%M:%SZ")" -v det="$details" '
        /execution_chain: \[\]/ {
            print
            print "  - step: " step
            print "    type: " stype
            print "    timestamp: " ts
            print "    details: " det
            next
        }
        { print }
    ' "$taskrun_file" > "$temp_file"
    mv "$temp_file" "$taskrun_file"
}

# Complete task run
complete_taskrun() {
    local taskrun_id="$1"
    local status="$2"  # success, failed, cancelled
    local result="$3"
    local duration_ms="$4"
    
    local taskrun_file="${MAWA_TASKRUN_DIR}/${taskrun_id}.yaml"
    
    if [ ! -f "$taskrun_file" ]; then
        return 1
    fi
    
    # Use awk for cross-platform compatibility
    awk -v st="$status" -v res="$result" -v dur="$duration_ms" '
        { 
            gsub(/status: running/, "status: " st)
            gsub(/result: null/, "result: " res)
            gsub(/duration_ms: 0/, "duration_ms: " dur)
            print
        }
    ' "$taskrun_file" > "${taskrun_file}.tmp"
    mv "${taskrun_file}.tmp" "$taskrun_file"
    
    # Update playbook metrics
    local skill_name=$(grep "skill:" "$taskrun_file" | head -1 | awk '{print $2}')
    local success="false"
    if [ "$status" = "success" ]; then
        success="true"
    fi
    update_playbook_metrics "$skill_name" "$success" "$duration_ms"
}

# Get recent task runs
get_recent_taskruns() {
    local limit="${1:-10}"
    
    ls -t "${MAWA_TASKRUN_DIR}"/*.yaml 2>/dev/null | head -"$limit" | while read -r f; do
        local id=$(basename "$f" .yaml)
        local task=$(grep "task_name:" "$f" | awk '{print $2}')
        local skill=$(grep "skill:" "$f" | head -1 | awk '{print $2}')
        local status=$(grep "status:" "$f" | head -1 | awk '{print $2}')
        echo "${id}: ${task} (${skill}) - ${status}"
    done
}

