mawa_status() {
    printf "${MAWA_CYAN}🐉 DragonStack MAWA Status${MAWA_NC}\n"
    echo "=========================="
    echo ""
    printf "${MAWA_BLUE}Version:${MAWA_NC} ${DRAGONSTACK_VERSION} (MAWA ${DRAGONSTACK_MAWA_VERSION})\n"
    echo ""
    
    printf "${MAWA_GREEN}Registered Skills:${MAWA_NC}\n"
    local count=0
    for reg in "${MAWA_REGISTRY_DIR}"/*.reg; do
        if [ -f "$reg" ]; then
            local name=$(basename "$reg" .reg)
            local version=$(grep "version:" "$reg" | head -1 | awk '{print $2}')
            local state=$(get_playbook_state "$name")
            echo "  ✓ ${name} v${version} [${state}]"
            count=$((count + 1))
        fi
    done
    echo ""
    echo "Total: $count skills registered"
    echo ""
    
    printf "${MAWA_YELLOW}Recent TaskRuns:${MAWA_NC}\n"
    get_recent_taskruns 5 | sed 's/^/  /'
}

mawa_inspect_skill() {
    local skill_name="$1"
    local reg_file="${MAWA_REGISTRY_DIR}/${skill_name}.reg"
    local playbook_file="${MAWA_PLAYBOOK_DIR}/${skill_name}.playbook"
    
    echo -e "${MAWA_CYAN}🔍 Inspecting Skill: ${skill_name}${MAWA_NC}"
    echo ""
    
    if [ -f "$reg_file" ]; then
        echo -e "${MAWA_GREEN}Registration:${MAWA_NC}"
        cat "$reg_file" | sed 's/^/  /'
    else
        echo -e "${MAWA_RED}  Not registered${MAWA_NC}"
    fi
    
    echo ""
    
    if [ -f "$playbook_file" ]; then
        echo -e "${MAWA_GREEN}Playbook:${MAWA_NC}"
        cat "$playbook_file" | sed 's/^/  /'
    else
        echo -e "${MAWA_RED}  No playbook${MAWA_NC}"
    fi
}

